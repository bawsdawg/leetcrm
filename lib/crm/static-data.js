function alloc(o) {
  const sum = Object.values(o).reduce((a, b) => a + b, 0);
  if (sum === 100) return o;
  const out = {};
  Object.entries(o).forEach(([k, v]) => {
    out[k] = Math.round((v / sum) * 100);
  });
  return out;
}

function npsHist(scores) {
  return scores.map((score, i) => ({
    score,
    sentAt: `2025-${String(5 + Math.floor(i / 2)).padStart(2, "0")}-${i % 2 === 0 ? "01" : "15"}`,
    respondedAt: `2025-${String(5 + Math.floor(i / 2)).padStart(2, "0")}-${i % 2 === 0 ? "03" : "17"}`,
  }));
}

